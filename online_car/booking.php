							<h2>TESLA MODEL 3</h2>
							<p>The Model 3 currently comes in three separate trims: a single-motor RWD powertrain in the Standard Range Plus, and two dual motor, AWD trims in the Long Range and Performance versions, The dual motor AWD Long Range Model 3 offers an estimated range of 353 miles, a top speed of 145 mph, and a 0-60 travel time of 4.2 seconds. This trim along with the Performance version comes with a larger 82 kWh battery </p>
							<p>Speaking of which, the dual motor Performance trim loses some mileage compared to the Long Range Model 3, offering an estimated range of 315 miles. However, where it lacks in range, it makes up for in speed, topping out at 162 mph and traveling 0-60 mph in 3.1 seconds (without rollout subtracted).
                              No matter what trim you choose, the Model 3 comes with seating for up to five, and offers 23 cubic feet of cargo space.</p>
							<p>The Standard Range Plus Model 3 weighs 3,582 lbs., while both the Long Range and Performance trims come in at 4,065 lbs. Impact Protection
								Combined with impact-absorbing rails and center pillar reinforcements, the strength and support of the rigid battery pack provides protection from every side.
								Model 3 is fully electric, so you never need to visit a gas station again. If you charge overnight at home, you can wake up to a full battery every morning. And when you’re on the road, it’s easy to plug in along the way—at any public station or with the Tesla charging network. We currently have over 35,000 Superchargers worldwide, with six new locations opening every week
								</p>
							<p>Tesla All-Wheel Drive has two independent motors for improved redundancy, each with only one moving part for minimal maintenance and maximum durability. Unlike traditional all-wheel drive systems, they digitally control torque to the front and rear wheels for far better handling and traction control.
								The inside of Model 3 is unlike any other car. You can use your smartphone as a key, and access all driver controls in the central 15-inch touchscreen. The all-glass roof extends from front to back, creating a sense of openness from every seat.


